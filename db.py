def normalize_name(name):
    """과목명 표기 차이를 보정."""
    if not name:
        return ""
    s = str(name).strip()
    # 공백 정규화
    import re
    s = re.sub(r"\s+", " ", s)
    return s


def lookup(name):
    """과목명으로 DB 조회. 정확 일치 → 공백 무시 일치."""
    if not name:
        return None
    s = normalize_name(name)
    if s in SUBJECT_DB:
        return SUBJECT_DB[s]
    # 공백 무시 매칭
    key_nospace = s.replace(" ", "")
    for k, v in SUBJECT_DB.items():
        if k.replace(" ", "") == key_nospace:
            return v
    return None
